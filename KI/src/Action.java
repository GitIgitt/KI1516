
public class Action {
	int changeX;
	int changeY;
	int changeZ;
	
	public Action(int x, int y, int z) {
		changeX = x;
		changeY = y;
		changeZ = z;
	}
	
	public int getChangeX() {
		return changeX;
	}
	
	public int getChangeY() {
		return changeY;
	}
	
	public int getChangeZ() {
		return changeZ;
	}
	
}
